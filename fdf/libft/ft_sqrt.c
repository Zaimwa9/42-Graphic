#include "libft.h"

int		ft_sqrt(int nb)
{
	int		i;

	i = 1;
	if (nb % 2 == 0)
		i = 2;
	if (nb < 0 || nb > 2147483647)
		return (0);
	while (i <= nb / 2 && i <= 46341)
	{
		if ((i * i) == nb)
			return (i);
		i = i + 2;
	}
	return (0);
}
