#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	size_t	i;
	size_t	n;

	i = 0;
	n = ft_strlen(s);
	if ((dest = (char*)malloc(sizeof(char) * (n + 1))) == NULL)
		return (NULL);
	while (i < n)
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
