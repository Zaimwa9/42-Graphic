#include "libft.h"

int		ft_isprint(int c)
{
	if ((ft_isalnum(c) == 1) || (c >= 32 && c <= 64) || \
			(c >= 123 && c <= 126) || (c >= 91 && c <= 96))
		return (1);
	else
		return (0);
}
